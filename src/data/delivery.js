// Deduplicate by name (keep first occurrence)
const seen = new Set();
export const LOCATIONS = RAW.filter(([name]) => {
  if (seen.has(name)) return false;
  seen.add(name);
  return true;
}).map(([name, home, courier]) => ({ name, home, courier }));

// Fast lookup map
const LOOKUP = new Map(LOCATIONS.map(l => [l.name, l]));

export function getDeliveryCharge(locationName, type) {
  const loc = LOOKUP.get(locationName?.toUpperCase());
  if (!loc) return type === 'home' ? 220 : 170; // fallback
  return type === 'home' ? loc.home : loc.courier;
}

export function searchLocations(query) {
  if (!query || query.length < 1) return [];
  const q = query.toUpperCase().trim();
  return LOCATIONS.filter(l => l.name.includes(q)).slice(0, 10);
}
