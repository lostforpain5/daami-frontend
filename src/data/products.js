export const getProductsByCategory = (category) => {
  if (category === 'new-arrivals') return products.filter(p => p.tags.includes('new-arrivals'));
  if (category === 'trending') return products.filter(p => p.tags.includes('trending'));
  return products.filter(p => p.category === category);
};

export const getProductById = (id) => products.find(p => p.id === parseInt(id));

export const getFeaturedProducts = () => products.filter(p => p.featured);

export const getRelatedProducts = (product, limit = 4) =>
  products.filter(p => p.category === product.category && p.id !== product.id).slice(0, limit);

export const formatPrice = (price) =>
  new Intl.NumberFormat('en-NP', { style: 'currency', currency: 'NPR', minimumFractionDigits: 0 }).format(price);
